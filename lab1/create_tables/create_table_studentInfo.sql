CREATE TABLE studentInfo(
      code_module VARCHAR,
      code_presentation VARCHAR,
      id_student INTEGER,
      gender CHAR,
      region VARCHAR,
      highest_education VARCHAR,
      imd_band VARCHAR,
      age_band VARCHAR,
      num_of_prev_attempts INTEGER,
      studied_credits INTEGER,
      disability CHAR,
      final_request VARCHAR,
      PRIMARY KEY(id_student)
);
